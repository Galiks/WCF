    [ServiceContract]
    public interface IService1
    {
        [OperationContract]
        Order GetOrderById(int id);
        [OperationContract]
        List<Order> GetOrders();
        [OperationContract]
        string GetData(string value);
        [OperationContract]
        Order GetCustomerById(int id);
        [OperationContract]
        CompositeType GetDataUsingDataContract(CompositeType composite);

